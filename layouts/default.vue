  data() {
    return {
      clipped: true,
      drawer: false,
      fixed: false,
      items: [
        {
          icon: 'mdi-apps',
          title: 'Main',
          to: 'index'
        },
        {
          icon: 'mdi-calendar-range',
          title: 'Timeline',
          to: 'timeline'
        },
        {
          icon: 'mdi-information',
          title: 'About',
