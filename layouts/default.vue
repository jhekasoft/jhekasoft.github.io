          </v-list-item-content>
        </v-list-item>
        <v-list-item
          v-for="item in availableLocales"
          :key="item.code"
          :to="switchLocalePath(item.code)"
          nuxt
          exact
        >
          <v-list-item-content>
            <v-list-item-title v-text="item.name" />
          </v-list-item-content>
        </v-list-item>
      </v-list>
    </v-navigation-drawer>
