        }
      ],
      miniVariant: false,
      right: true,
      rightDrawer: false,
      title: 'Jhekasoft'
    }
  },
  computed: {
    availableLocales() {
      return this.$i18n.locales.filter((i) => i.code !== this.$i18n.locale)
